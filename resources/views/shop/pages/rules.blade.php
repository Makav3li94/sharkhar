                                    تبصره 14- فروشنده ای که حداقل یک پیشنهاد قیمت معادل یا بالاتر از قیمت پایه کالا دریافت کند متعهد به فروش کالا به بالاترین قیمت ارائه شده می باشد مگر اینکه:
                                    <br>
                                    <ul>
                                        <li>نام کالا در لیست کالاهای ممنوعه باشد.
                                        </li>
                                        <li>خریداری که بالاترین قیمت را پیشنهاد نموده از عهده شرایط اعلام شده توسط فروشنده بر نیامده باشد. (مثلا روش پرداخت بهای کالا) و یا براساس مقررات و قوانین کشور مجاز به خریداری کالا نباشد.
                                        </li>
                                        <li>مشخصات و شناسایی خریدار برای فروشنده امکان پذیر نباشد.
                                        </li>
                                    </ul>
                                    <br>
                                    <br>
                                    تبصره 15- کاربر حق اخلال در روند سالم مزایدات و دستکاری در قیمت کالاهایی که سایر اعضا آنها را برای فروش ارائه نموده اند ، نداشته  و برای کالاهایی که شخصا برای فروش ارائه می نماید، نه بصورت مستقیم  ونه غیرمستقیم مجاز به پیشنهاد قیمت نیست. ضمنا تبانی با دیگران جهت  ارائه پیشنهاد  قیمت ، برای کالای خود و یا استفاده از نام کاربری دیگران مجاز نخواهد بود.
                                    <br>
                                    <br>
                                    تبصره 16- انجام معامله خارج از سایت شرخر به هر شکلی مجاز نمی باشد. تبادل هرگونه اطلاعات تماس و یا دادن پیشنهاد جهت انجام معامله خارج از سایت شرخر ممنوع است. همینطور استفاده از هرگونه متنی شامل لینک های خرید سایت های دیگر و یا درج هرگونه اطلاعات تماس برای کاربران جهت تبلیغات یا ارایه خدمات خود مجاز نمی باشد.

                            </p>

                            <p>
                                4-9- روش ارسال کالا
                            </p>
                            <p>
                                فروشندگان باید پس از فروش کالا نسبت به ارسال آن به خریدار از طریق پست های مورد تایید شرخر اقدام نمایند. این شرکت ها شامل پست پیشتاز جمهوری اسلامی، شرکت تیپاکس و شرکت TPG میباشند.
                            </p>

                            <p>
                               ;4-10- مسئولیت فروشندگان
                            </p>
                            <p>
                               کاربر شرخر که به عنوان فروشنده فعالیت مینماید، موظف است نسبت به ارسال  و تحویل کالا  اقدام نماید. بدیهی است که هرگونه مسئولیت، در زمینه کیفیت یا کمیت کالای ارائه شده و نیز ارسال آن بر عهده فروشنده بوده و در صورت اعلام  خریدار ، مبنی بر عدم دریافت کالا، فروشنده موظف به ارائه رسید ارسال کالا میباشد. فروشنده موظف است اطلاعات کامل خریدار را از سایت دریافت نماید. در بعضی از مواقع این امکان وجود دارد ، شرخر به منظور تطبیق اطلاعات ، جلوگیری از سوء استفاده و کلاه برداری با فروشنده و خریدار به صورت جداگانه تماس گرفته و در این صورت هر دو طرف موظف به  ارائه  اطلاعات به شرخر خواهند بود. هر نوع ضرر و زیانی که به هر نحو از سوی فروشنده به خریدار و یا از سوی خریدار به فروشنده وارد شود میبایست مابین طرفین حل و فصل شود. شرخر امکان گفتگوی دو طرفه را به منظور حل مشکلات از طریق بخش تیکت فراهم نموده و در صورت عدم حل مشکل، امکان پیگیری قضایی برای طرفین وجود دارد. چنانچه  در نتیجه نقض این توافق نامه ضرر و زیانی از سوی هر یک از طرفین به شرخر، سایر کاربران و یا اشخاص ثالث وارد شود، مسئولیت جبران خسارت با ایشان خواهد بود.
                                    <br>
                                    <br>
                                    <ol>
                                        <li>فروشنده موظف خواهد بود کالاهایی را که موجود نداشته در قسمت فروشگاه وارد ننموده و  یا چنانچه موجودی کالایی در فروشگاه به صفر رسیده و امکان تهیه مجدد آن طی 24 ساعت وجود نداشته باشد نسبت به حذف آن کالا اقدام نماید.
                                        </li>
                                        <li>از آنجاییکه هزینه حمل از خریدار دریافت می گردد، فروشنده موظف خواهد بود در هنگام تحویل کالا هزینه ارسال را نقدا پرداخت نماید. پس از وصول کالا توسط خریدار ، هزینه حمل دریافت شده از خریدار به همراه وجه کالا پس از کسر کسورات ، به حساب فروشنده واریز خواهد شد.
                                        </li>
                                        <li>فروشنده موظف خواهد بود فقط از طریق یکی از راه های فوق، جهت ارسال کالا برای خریدار اقدام نماید.
                                        </li>
                                        <li>در صورتیکه کالای ارسال شده جهت خریدار به دلایلی از قبیل مغایرت با اطلاعات وارد شده کالا در سایت، شکستگی و یا آسیب دیدگی از سوی خریدار مرجوع گردد، هیچ هزینه ایی به فروشنده از سوی سایت پرداخت نخواهد شد.
                                        </li>
                                        <li>فروشندگان ملزم هستند در معاملاتی که در این سایت انجام می دهند الزامات این توافق نامه را مراعات نمایند و مندرجات این توافق نامه در تفسیر و تعبیر اختلافات طرفین ملاک عمل می باشد.
                                        </li>
                                        <li>فروشنده ملزم می باشد در زمان مفقود شدن کالا، جهت استفاده از خدمات بیمه شرکت های حمل و نقل با توجه به قیمت کالا اقدام نماید.
                                        </li>
                                    </ol>
                            </p>

                            <p>
                                4-11- مسئولیت خریداران :
                            </p>
                            <p>
                                
                                    <ol>
                                        <li>در هنگام به وجود آمدن اختلاف بین خریدار و فروشنده، چنانچه طرفین به توافق نرسند، شکایت به بخش حل اختلاف ارجاع داده می شود. اگر طبق نظر بخش حل اختلاف مقرر شد که کالا باید مرجوع شود، هزینه حمل برای استرداد کالا به فروشنده به عهده طرفی خواهد بود که بخش حل اختلاف رای داده است. در صورتی که خریدار ظرف مدت 48 ساعت آن کالا را مرجوع نکند، مبلغ پرداختی به خریدار مسترد نخواهد شد و وجه برای فروشنده قابل برداشت خواهد بود.
                                        </li>
                                        <li>خریدار در هنگام استرداد کالا موظف خواهد بود، فقط از طریق شرکت پست جمهوری اسلامی ایران، شرکت تیپاکس و یا تی پی جی جهت این امر استفاده نموده و اطلاعات بارنامه ارسالی را ظرف 24 ساعت کاری به سایت اعلام نماید. پس از تایید وصول کالا توسط فروشنده، وجه پرداختی بابت خرید آن کالا به حساب خریدار واریز خواهد شد.
                                        </li>
                                        <li>خریدارانی که امتیاز آنها کمتر از 10 باشد حق شرکت در بیش از دو مزایده را نخواهند داشت.
                                        </li>
                                        <li>خریداران ملزم هستند در معاملاتی که در این سایت انجام می دهند الزامات این توافق نامه را مراعات نمایند و مندرجات این توافق نامه در تفسیر و تعبیر اختلافات طرفیت ملاک عمل می باشد.
                                        </li>
                                    </ol>
                            </p>

                            <p>
                                4-12- روش پرداخت
                                
                            </p>
                            <p>
                                خریداران می توانند با استفاده از کلیه کارت های عضو شتاب نسبت به خرید کالا های مورد نیاز خود اقدام نمایند.
                                    <br>
                                    پس از واریز وجه توسط خریدار، قسمت حسابداری سایت از وصول پرداختی شما در حساب بانک اطمینان حاصل نموده و چنانچه پول از حساب شما کسر گردیده، ولی به حساب سایت واریز نشده باشد، طی تماس تلفنی این موضوع به فرستنده وجه اطلاع داده خواهد شد. خریدار نهایتا پس از اطلاع از این موضوع تا 48 ساعت صبر نموده و اگر طی این مدت وجه واریزی از سوی بانک  مسترد نگردید، آنگاه با پشتیبانی بانک عامل پرداخت اینترنتی تماس گرفته و نسبت به رفع مغایرت اقدام نماید.
                                    <br>
                                    <br>
                                    تبصره 17- برای جلوگیری از زیان‌ها و خطرات احتمالی، سایت می‌تواند شیوه‌های پرداخت یک تراکنش را محدود نماید. در چنین مواردی، كاربر می‌تواند پس از اطلاع از این محدودیت‌ها، با استفاده از شیوه‌های باقی‌مانده تراکنش را انجام دهد.
                                
