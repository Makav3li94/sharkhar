                            <p>
                                4-15- روش تسویه حساب با فروشندگان
                                
                            </p>
                            <p>
                                پس از فروش موفق کالا در سایت، مبلغ کالا و هزینه ارسال (هزینه ارسال به صورت تقریبی محاسبه می شود و الزاما با هزینه های ارسال فروشنده مطابقت ندارد) از خریدار دریافت گردیده و با ایمیل و اس ام اس به فروشنده اطلاع داده خواهد شد تا کالای خود را برای خریدار طبق سفارش داده شده، در زمان تعیین شده ارسال نماید. پس از تحویل و تایید کالا توسط خریدار، مبلغ واریز شده پس از کسر تعرفه خدمات و مالیات و عوارض ارزش افزوده به قسمت مبلغ قابل برداشت فروشنده اضافه می گردد. فروشندگان در صفحه صورتحساب، در قسمت حساب شرخر من ، درخواست انتقال وجه مبلغ قابل برداشت خود را ارسال نموده و پس از طی 48 ساعت کاری از تاریخ ارسال درخواست، مبلغ قابل برداشت، به حساب اعلام شده در صفحه مشخصات بانکی واریز می گردد.
                                    <br>
                                    لازم به ذکر است تسویه حساب با کاربران در صورتی که با سایر بندهای توافق نامه کاربری مغایرت نداشته باشد، تنها از طریق شماره شبای ثبت شده در حساب کاربری در سایت شرخر انجام می شود. همینطور لازم است نام، نام خانوادگی و شماره ملی کاربر در حساب کاربری ایشان ثبت شده باشد و شماره شبای ثبت شده متعلق به نام، نام خانوادگی و شماره ملی درج شده در حساب کاربری باشد. در غیر این صورت وجه واریز نخواهد شد.
                                    <br>
                                    اگر خریدار دریافت کالا را تا 5 روز پس از ثبت بارنامه توسط فروشنده تایید نکند، در این مدت مبلغ پرداختی به حساب فروشنده واریز نخواهد شد. پس از سپری شدن این مدت و تایید نشدن کالا توسط خریدار، از بخش پشتیبانی شرخر برای تایید تلفنی کالا با خریدار تماس گرفته می شود. چنانچه طی مدت 24 ساعت دو بار بخش پشتیبانی با خریدار تماس بگیرد و خریدار پاسخگو نباشد، سیستم به صورت خودکار کالا را تایید می کند و مبلغ واریزی به حساب فروشنده واریز می شود.
                                    <br>
                                    اگر خریدار هنگام تایبد کالا فیدبک ناراضی بدهد، مبلغ کالا تا 7 روز پس از تاریخ ثبت بارنامه توسط فروشنده، غیر قابل برداشت است و طی این مدت خریدار باید شکایت خود را مطرح کند. در صورت مطرح کردن شکایت، اختلاف به سایت شرخر ارجاع داده می شود. سپس سایت با توجه به ادله دو طرف و طبق قوانین موجود دارای حق تصمیم گیری در مورد آزادسازی بخشی و یا تمامی مبلغ مبادله برای هر دو طرف و یا تنها یک طرف منازعه می باشد.
                                    <br>
                                    در صورتی که خریدار طی این مدت (یعنی 7 روز) شکایت نکند، پس از آن مبلغ واریزی برای فروشنده قابل برداشت می شود.
                                    <br>
                                    <br>
                                    <span class="styleBlackB">موجودی حساب فروشنده،</span> مبلغی است که خریدار بابت خرید کالا به شرکت شرخر پرداخت کرده ولی کالا را دریافت نکرده یا دریافت کرده و تائید نکرده و یا از خرید خود راضی نبوده و به شرخر شکایت کرده، این وجه بعنوان امانت در حساب شرکت باقی مانده و قابل برداشت نخواهد بود.
                                
                            </p>
                            <p>
                                4-16- امتیازات اعضاء
                                
                            </p>
                            <p>
                                پس از مبادله هر کالا، خریدار و فروشنده می توانند نظرات مثبت، خنثی و یا منفی خود را در خصوص روش عملکرد طرف مقابل ارائه نموده و آنرا در سوابق طرف مقابل در کنار نام کاربری جهت اطلاع سایرین ثبت نمایند. روش "امتیاز کاربر" که در سایت مورد استفاده قرار گرفته به کاربران کمک می نماید تا سابقه سایر اعضاء را مشاهده نمایند. به کاربران توصیه میشود قبل از معامله با هر کاربر "امتیاز کاربر" مورد نظر را بررسی نمایند. اعلام نظر مثبت ، یک امتیاز مثبت، خنثی صفر امتیاز  و منفی یک امتیاز منفی برای  طرف مقابل محاسبه می شود . سایت مجاز خواهد بود در صورتی که امتیاز هر یک از اعضا به 2- برسد، به صورت اتوماتیک حساب کاربری آن عضو را معلق نموده و حق استفاده از خدمات سایت را سلب  نماید. امتیاز هر عضو در حقیقت شهرت و اعتبار وی در سایت می باشد که سایرین می توانند بر اساس آن در خصوص قابل اطمینان بودن یا نبودن وی بعنوان طرف یک معامله قضاوت نمایند. در صورت پرداخت وجه کالا توسط خریدار، سایت به صورت خودکار یک امتیاز مثبت برای وی در نظر میگیرد.
                                
                            </p>


                                <h3>ماده 5- حل اختلافات
                                
                                </h3>
                            <p>
                                خریداران و فروشندگان در صورت داشتن  شکایت، اختلاف و یا مواجه با بروز مشکلات در امر خرید و فروش می بایست با مراجعه به بخش حل اختلاف که دسترسی به آن از طریق صفحه  "شرخر من" میسر شده،  مورد حمایت شرخر قرار گیرند.
                                
                            </p>

                            <p>
                                حل اختلافات از طریق داوری شرخر
                                
                            </p>
                            <p>
                                در سایت یک سیستم پرسش و پاسخ ایجاد گردیده که در صورت بروز مشکل، خریدار و فروشنده موظف خواهند بود تا از این طریق مشکلات شان را با یکدیگر مطرح و اقدام به حل آن نمایند. این سیستم شکایت شاکی را ثبت نموده و شماره  پیگیری جهت اقدامات بعدی صادر می نماید. قسمت حل اختلاف سایت با استفاده از این سیستم قادر خواهد بود تا در جریان اختلاف ایجاد شده و ریز گفتگوی طرفین قرار گرفته و در نهایت نسبت به صدور رای عادلانه اقدام نماید. سایت  مسئولیتی در قبال حل اختلاف ، در خارج از این چهارچوب تعیین شده نخواهد داشت.
                                    <br>
                                    <br>
